// 5. Escribe un programa de tres líneas que pida un número, pida otro número y
// escriba el resultado de sumar estos dos números.

let primerNumero = Number(prompt("Ingrese el primer número a sumar", ""));
let segundoNumero = Number(prompt("Ingrese el segundo número a sumar", ""));
